my_tuple = (1, "Python", 3.14, "Code", 42)
print("The tuple is:", my_tuple)
first_three = my_tuple[:3]
print("The first three elements are:", first_three)
last_two = my_tuple[-2:]
print("The last two elements are:", last_two)
a, b, c = my_tuple[:3]
print("Unpacked values:")
print("a =", a)
print("b =", b)
print("c =", c)
